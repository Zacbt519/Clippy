public class Weather{

    /// <summary>
    /// Current temperature
    /// </summary>
    public double temp;

    /// <summary>
    /// Temperature with humidity or wind chill
    /// </summary>
    public double feels_like;

    /// <summary>
    /// Minimum temperature
    /// </summary>
    public double temp_min;

    /// <summary>
    /// Maximum temperature
    /// </summary>
    public double temp_max;

    /// <summary>
    /// Atmospheric pressure
    /// </summary>
    public double pressure;

    /// <summary>
    /// Humidity
    /// </summary>
    public double humidity;
}